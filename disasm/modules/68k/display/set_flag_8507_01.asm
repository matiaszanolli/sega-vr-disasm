; ============================================================================
; Set Display Flag $01
; ROM Range: $00246C-$002472 (8 bytes)
; ============================================================================
; Sets display mode flag at $8507 to $01.
;
; Entry: none
; Uses: none
; ============================================================================

set_flag_8507_01:
        move.b  #$01,($FFFF8507).w      ; $11FC $0001 $8507
        rts
