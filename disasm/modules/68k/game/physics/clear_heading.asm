; ============================================================================
; Clear Heading Register
; ROM Range: $00909C-$0090A4 (8 bytes)
; ============================================================================
; Clears heading register at $8000.
;
; Entry: none
; Uses: none
; ============================================================================

clear_heading:
        move.w  #$0000,($FFFF8000).w    ; $31FC $0000 $8000
        rts
