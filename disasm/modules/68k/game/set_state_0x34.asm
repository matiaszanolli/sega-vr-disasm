; ============================================================================
; Set Game State $34
; ROM Range: $0033E4-$0033EA (8 bytes)
; ============================================================================
; Sets game state byte at $C305 to $34.
;
; Entry: none
; Uses: none
; ============================================================================

set_state_0x34:
        move.b  #$34,($FFFFC305).w              ; $0033E4  set state to $34
        rts                                     ; $0033EA
