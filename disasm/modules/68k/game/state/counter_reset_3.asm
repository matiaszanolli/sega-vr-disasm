; ============================================================================
; Reset Step Counter to 3
; ROM Range: $004630-$004638 (8 bytes)
; ============================================================================
; Resets step counter at $C819 to 3.
;
; Entry: none
; Uses: none
; ============================================================================

counter_reset_3:
        move.b  #$03,($FFFFC819).w      ; $11FC $0003 $C819
        rts
