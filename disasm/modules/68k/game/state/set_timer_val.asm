; ============================================================================
; Set Timer Value (20)
; ROM Range: $00581A-$005822 (8 bytes)
; ============================================================================
; Sets timer/counter at $C8C5 to 20.
;
; Entry: none
; Uses: none
; ============================================================================

set_timer_val:
        move.b  #$14,($FFFFC8C5).w      ; $11FC $0014 $C8C5
        rts
